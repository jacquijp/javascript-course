//Primitive data type - immutable
let number = 23
number = number + 10
console.log(number)

let isTrue = true
isTrue = false
console.log(isTrue)

//Complex data type - Mutable
let user = { name: 'Peter', age: 15 }
user.age = 20
console.log(user)

let fruits = ['apple', 'orange']
fruits[0] = 'grape'
console.log(fruits)

